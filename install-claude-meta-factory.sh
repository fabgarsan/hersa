#!/usr/bin/env bash
# Claude Code Meta-Factory Installer
# Self-contained: embeds 2 agents, 5 skills, 2 scripts, the architecture guide,
# and a CLAUDE.md registry fragment. Run from the root of the repo where you
# want it installed (or pass the target as the first argument).
#
# Usage:
#   ./install-claude-meta-factory.sh [TARGET_DIR] [--force] [--quiet]
#
# Examples:
#   ./install-claude-meta-factory.sh                  # install in cwd
#   ./install-claude-meta-factory.sh ~/my-project     # install in ~/my-project
#   ./install-claude-meta-factory.sh . --force        # overwrite existing files
#
# What it does (idempotent — safe to re-run):
#   1. Creates .claude/{agents,skills} structure
#   2. Writes 10 component files (skips any that already exist unless --force)
#   3. Makes helper scripts executable
#   4. Creates or appends CLAUDE.md (merges registry fragment with markers)
#   5. Creates .claudeignore with standard exclusions if absent
#   6. Reports next steps

set -euo pipefail

# ─────────────────────────────────────────────────────────────────────────────
# Argument parsing
# ─────────────────────────────────────────────────────────────────────────────

TARGET="."
FORCE=0
QUIET=0

# First non-flag arg is target; subsequent flags configure behavior
target_set=0
for arg in "$@"; do
  case "$arg" in
    --force) FORCE=1 ;;
    --quiet) QUIET=1 ;;
    --help|-h)
      sed -n '2,22p' "$0" | sed 's/^# \?//'
      exit 0
      ;;
    -*)
      echo "Unknown flag: $arg" >&2
      echo "Run with --help for usage." >&2
      exit 2
      ;;
    *)
      if [[ $target_set -eq 0 ]]; then
        TARGET="$arg"
        target_set=1
      else
        echo "Unexpected argument: $arg" >&2
        exit 2
      fi
      ;;
  esac
done

if [[ ! -d "$TARGET" ]]; then
  echo "ERROR: target directory does not exist: $TARGET" >&2
  exit 2
fi

TARGET="$(cd "$TARGET" && pwd)"

log() { [[ $QUIET -eq 1 ]] || echo "$@"; }

log ""
log "Claude Code Meta-Factory Installer"
log "Target: $TARGET"
log ""

# ─────────────────────────────────────────────────────────────────────────────
# Pre-flight: detect base64 decode flag (Linux: -d, macOS/BSD: -D, GNU: --decode)
# ─────────────────────────────────────────────────────────────────────────────

if ! command -v base64 >/dev/null 2>&1; then
  echo "ERROR: base64 not found in PATH." >&2
  exit 3
fi

B64_DECODE=""
for flag in "--decode" "-d" "-D"; do
  if echo "dGVzdA==" | base64 $flag >/dev/null 2>&1; then
    B64_DECODE="$flag"
    break
  fi
done

if [[ -z "$B64_DECODE" ]]; then
  echo "ERROR: cannot determine base64 decode flag for this system." >&2
  exit 3
fi

# ─────────────────────────────────────────────────────────────────────────────
# File-writing helper
# ─────────────────────────────────────────────────────────────────────────────

WROTE_COUNT=0
SKIP_COUNT=0

write_file() {
  local rel="$1" b64="$2" exec="$3"
  local dest="$TARGET/$rel"
  local dir
  dir="$(dirname "$dest")"

  mkdir -p "$dir"

  if [[ -e "$dest" && $FORCE -eq 0 ]]; then
    log "  SKIP (exists):    $rel"
    SKIP_COUNT=$((SKIP_COUNT + 1))
    return 0
  fi

  echo "$b64" | base64 $B64_DECODE > "$dest"
  if [[ "$exec" == "1" ]]; then
    chmod +x "$dest"
  fi
  log "  WROTE:            $rel"
  WROTE_COUNT=$((WROTE_COUNT + 1))
}

